package com.example;

public class EixoValor {
    public String numEixo;
    public String cargaDescarga;
    public String deslocamento;

    public String getCargaDescarga() {
        return this.cargaDescarga;
    }

    public void setCargaDescarga(String cargaDescarga) {
        this.cargaDescarga = cargaDescarga;
    }

    public String getDeslocamento() {
        return this.deslocamento;
    }

    public void setDeslocamento(String deslocamento) {
        this.deslocamento = deslocamento;
    }

    public String getNumEixo() {
        return this.numEixo;
    }

    public void setNumEixo(String numEixo) {
        this.numEixo = numEixo;
    }

}